package folder;

public class Practice {

	public static void main(String[] args) {
		
		System.out.println("문자열 입니다.");
		// ()안에 내용을 출력하고 줄바꿈 O
		
		System.out.print("안녕");
		// ()안데 내용을 출력하고 줄바꿈 X
		System.out.print("하세요");
		
		System.out.println();
		// 이렇게만 썼을 경우 줄바꿈만 한다.
		
		System.out.println("1+5");
		// " " 안에 넣은 내용은 그대로 출력
		
		System.out.println(1+5);
		// ()안에 값을 계산 후 6을 출력
		
		
		// 이름 나이 키 몸무게를 문자열로 출력하기
		System.out.println("이름 : 꾸꾸까까");
		System.out.println("나이 : 27살");
		System.out.println("키 : 170");
		System.out.println("몸무게 : 48kg");
		
		
		
	}
	
	
}
